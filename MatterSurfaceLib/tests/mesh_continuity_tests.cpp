static int ifloor(float v) { return (int)floorf(v); }

// Mirror of Cell::intersects_sphere against the box of integer coord (cx,cy,cz).
static bool box_intersects_sphere(int cx, int cy, int cz, float s, Vector3 c, float r) {
    float minx = cx*s, miny = cy*s, minz = cz*s;
    float maxx = (cx+1)*s, maxy = (cy+1)*s, maxz = (cz+1)*s;
    float qx = fmaxf(minx, fminf(c.x, maxx));
    float qy = fmaxf(miny, fminf(c.y, maxy));
    float qz = fmaxf(minz, fminf(c.z, maxz));
    float dx = c.x-qx, dy = c.y-qy, dz = c.z-qz;
    return (dx*dx+dy*dy+dz*dz) <= r*r;
}

