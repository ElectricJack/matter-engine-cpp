    g_unexpected += mat_fail;
    printf("%s\n", mat_fail == 0 ? "PASS" : "FAIL");
    printf("\n");

    // Clip field carve regression: a foreign clip particle must pull the
    // group's isosurface inward on the side facing it.
    printf("clip_carves_surface: ");
    int clip_fail = test_clip_carves_surface();
    g_unexpected += clip_fail;
    printf("%s\n", clip_fail == 0 ? "PASS" : "FAIL");
    printf("\n");

    // Clip-normal agreement with non-zero blendWidth: the normal override must
    // fire on the SAME blended field value the scalar clip carves, not the hard
    // min. Fails under the old -fO > fmin trigger when blendWidth>0.
    printf("clip_normals_blended: ");
    int clipn_fail = test_clip_normals_blended();
    g_unexpected += clipn_fail;
    printf("%s\n", clipn_fail == 0 ? "PASS" : "FAIL");
    printf("\n");

    const float s = 4.0f;  // smallest cell size (size_power 0)
    const uint32_t M0 = 0, M1 = 1;

    std::vector<Case> cases;

    // E1: single particle fully inside its owning cell. Baseline.
    cases.push_back({ "E1", "single particle centered in cell, r<s/2",
        { { {2,2,2}, 1.5f, M0 } }, s, true, true });

    // E2: single particle straddling ONE face (x=4 between cell 0 and 1).
    cases.push_back({ "E2", "single particle straddling one face",
        { { {4.0f,2,2}, 1.5f, M0 } }, s, true, true });

    // E3: straddling an edge (x=4 and y=4 -> 4 cells share).
    cases.push_back({ "E3", "single particle straddling an edge",
        { { {4.0f,4.0f,2}, 1.5f, M0 } }, s, true, true });

    // E4: straddling a corner (x=y=z=4 -> 8 cells share).
    cases.push_back({ "E4", "single particle straddling a corner",
        { { {4.0f,4.0f,4.0f}, 1.5f, M0 } }, s, true, true });

    // E5: particle larger than a cell (spans several cells).
    cases.push_back({ "E5", "particle radius > cell size",
        { { {2,2,2}, 5.0f, M0 } }, s, true, true });

    // E6: particle exactly on a cell face plane origin (pos = C*s).
    cases.push_back({ "E6", "particle centered exactly on cell corner",
        { { {0.0f,0.0f,0.0f}, 1.5f, M0 } }, s, true, true });

    // E7: two same-material particles overlapping across a cell boundary.
    cases.push_back({ "E7", "two same-material particles overlapping across boundary",
        { { {3.0f,2,2}, 1.5f, M0 }, { {5.0f,2,2}, 1.5f, M0 } }, s, true, true });

    // E8: two DIFFERENT-material particles overlapping (bands expected; separate
    // material meshes -> not asserted watertight, just observed).
    cases.push_back({ "E8", "two different-material particles overlapping",
        { { {3.0f,2,2}, 1.5f, M0 }, { {5.0f,2,2}, 1.5f, M1 } }, s, false, false, false });

    // E9: minimal assignment(r)-vs-field(2.5r) mismatch. Face at x=4 between
    // cell0 [0,4] and cell1 [4,8]. P1 sits in cell0 1.7 from the face (>r, so it
    // does NOT intersect cell1 -> NOT assigned there), P2 sits in cell1 and DOES
    // intersect cell0. The two spheres overlap (dist 2.7 < 3.0) so the blended
    // surface bulges across x=4 -- but cell1's field omits P1, so the two cells
    // contour the shared face from different particle sets.
    cases.push_back({ "E9", "overlap blended across a face where one particle is unassigned",
        { { {2.3f,2,2}, 1.5f, M0 }, { {5.0f,2,2}, 1.5f, M0 } }, s, true, true });

    // E10: dense small cloud spanning many cells (integration / regression).
    // The 3x3x3 lattice (spacing 1.6, r=1.2) leaves 8 genuine internal voids:
    // each 2x2x2 sub-cube's center is sqrt(3)*0.8 = 1.386 from its 8 corner
    // particles, i.e. 0.186 OUTSIDE every sphere, so the analytic union encloses
