// Headless reproduction harness for the remaining per-cell meshing artifacts
// seen in screenshot 2026-06-13 104038 (flat caps, floating faceted blobs,
// hard bands). It faithfully replicates the cluster's three-radius pipeline
// WITHOUT a GL context (no UploadMesh / BLAS), since those layers are not under
// test:
//
//   * cell CREATION  -> mark_cells_dirty_around_particle: box floor((p +/- 2r)/s)
//                       (cluster.cpp:132-160, influence_radius = 2*r)
//   * particle ASSIGN-> intersects_sphere, closest-point-on-AABB <= r
//                       (cluster.cpp:246-251 + cell.cpp:83-95)
//   * field EVAL     -> CalculateScalarAndMaterial search radius 2.5*r
//                       (surface.c:713)
//   * cell box / mesh-> center=(C+0.5)*s, size=s, divisionPow=4
//                       (cell.cpp:59-75, 271-278)
//
// Meshes are produced by the real GenerateMesh + simplify_mesh, so any seam,
// crack, clipped cap, or floating component is generated by the genuine
// algorithm. Oracles run on the combined CPU triangle soup:
//
//   1. boundary-edge count  : welded edges used by exactly 1 triangle. A closed
//                             blob -> 0. Cracks / flat caps at a cell face show
//                             up as a ring of boundary edges.
//   2. connected components : floating blobs that don't blend -> >1 component.
//   3. coverage gap         : analytic union-of-spheres surface points whose
//                             nearest mesh triangle is farther than tolerance
//                             -> a clipped / missing region.

